SELECT
	CASE WHEN EXISTS(
		SELECT
			*
		FROM
			[Parent] [p]
		WHERE
			[p].[ParentID] = 5 AND ([p].[Value1] IS NULL AND COALESCE(CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END, CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END) IS NOT NULL OR [p].[Value1] IS NOT NULL AND COALESCE(CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END, CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END) IS NULL OR [p].[Value1] <> COALESCE(CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END, CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END))
	) THEN 1 ELSE 0 END

