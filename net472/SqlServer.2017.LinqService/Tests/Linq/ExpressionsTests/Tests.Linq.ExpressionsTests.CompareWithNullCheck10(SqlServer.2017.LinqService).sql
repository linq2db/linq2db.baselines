SELECT
	CASE WHEN EXISTS(
		SELECT
			*
		FROM
			[Parent] [p]
		WHERE
			[p].[ParentID] = 5 AND (COALESCE(CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END, CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END) <> [p].[Value1] OR COALESCE(CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END, CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END) IS NULL AND [p].[Value1] IS NOT NULL OR COALESCE(CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END, CASE WHEN N'not5' IS NULL THEN NULL ELSE CAST(N'6' AS INT) END) IS NOT NULL AND [p].[Value1] IS NULL)
	) THEN 1 ELSE 0 END

