	SUM(t."IntValue") OVER (ORDER BY t."Id" RANGE BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING),
	SUM(t."IntValue") OVER (ORDER BY t."Id" RANGE BETWEEN CURRENT ROW AND UNBOUNDED FOLLOWING)
