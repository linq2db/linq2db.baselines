SELECT
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) as c1,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) as c2,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN UNBOUNDED PRECEDING AND 3 FOLLOWING) as c3,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN CURRENT ROW AND CURRENT ROW) as c4,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN CURRENT ROW AND UNBOUNDED FOLLOWING) as c5,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN CURRENT ROW AND 3 FOLLOWING) as c6,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN 2 PRECEDING AND CURRENT ROW) as c7,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN 2 PRECEDING AND UNBOUNDED FOLLOWING) as c8,
	SUM(t.IntValue) OVER (ORDER BY t.Id ROWS BETWEEN 2 PRECEDING AND 3 FOLLOWING) as c9
FROM
	WindowFunctionTestEntity t

