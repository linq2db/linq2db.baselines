SELECT * FROM (
	SELECT
		p.SPECIFICNAME,
		p.PROCSCHEMA,
		p.PROCNAME,
		p.TEXT,
		p.REMARKS,
		'P' AS IS_PROCEDURE,
		o.OBJECTMODULENAME,
		CASE WHEN CURRENT SCHEMA = p.PROCSCHEMA THEN 1 ELSE 0 END,
		p.PARM_COUNT
	FROM
		SYSCAT.PROCEDURES p
		LEFT JOIN SYSCAT.MODULEOBJECTS o ON p.SPECIFICNAME = o.SPECIFICNAME
	WHERE p.PROCSCHEMA = 'DB2INST1' AND p.PROCSCHEMA NOT IN ('SYSPROC', 'SYSIBMADM', 'SQLJ', 'SYSIBM')
	UNION ALL
	SELECT
		f.SPECIFICNAME,
		f.FUNCSCHEMA,
		f.FUNCNAME,
		f.BODY,
		f.REMARKS,
		f.TYPE,
		o.OBJECTMODULENAME,
		CASE WHEN CURRENT SCHEMA = f.FUNCSCHEMA THEN 1 ELSE 0 END,
		f.PARM_COUNT
	FROM
		SYSCAT.FUNCTIONS f
		LEFT JOIN SYSCAT.MODULEOBJECTS o ON f.SPECIFICNAME = o.SPECIFICNAME
		WHERE f.FUNCSCHEMA = 'DB2INST1' AND f.FUNCSCHEMA NOT IN ('SYSPROC', 'SYSIBMADM', 'SQLJ', 'SYSIBM') AND f.FUNCSCHEMA NOT IN ('SYSPROC', 'SYSIBMADM', 'SQLJ', 'SYSIBM'))
ORDER BY OBJECTMODULENAME, PROCSCHEMA, PROCNAME, PARM_COUNT

