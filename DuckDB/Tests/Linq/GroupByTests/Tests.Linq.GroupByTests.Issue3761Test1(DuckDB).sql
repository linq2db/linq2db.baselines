SELECT
	t1.Year_1,
	t1.Month_1,
	SUM(t1.SKUPAJ)
FROM
	(
		SELECT
			EXTRACT(year FROM CASE
				WHEN n.DATUM IS NOT NULL THEN n.DATUM
				ELSE '0001-01-01 00:00:00.000000'::TIMESTAMP
			END) as Year_1,
			EXTRACT(month FROM CASE
				WHEN n.DATUM IS NOT NULL THEN n.DATUM
				ELSE '0001-01-01 00:00:00.000000'::TIMESTAMP
			END) as Month_1,
			n.SKUPAJ
		FROM
			Issue3761Table n
		WHERE
			n.DATUM < make_timestamp(2019, 1, 1, 0, 0, 0)
	) t1
GROUP BY
	t1.Year_1,
	t1.Month_1

