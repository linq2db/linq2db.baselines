SELECT
	"rc".RDB$RELATION_NAME,
	"rc".RDB$CONSTRAINT_NAME,
	"seg".RDB$FIELD_NAME,
	"seg".RDB$FIELD_POSITION
FROM
	RDB$RELATION_CONSTRAINTS "rc"
		INNER JOIN RDB$INDEX_SEGMENTS "seg" ON "rc".RDB$INDEX_NAME = "seg".RDB$INDEX_NAME
WHERE
	"rc".RDB$CONSTRAINT_TYPE = 'PRIMARY KEY'

