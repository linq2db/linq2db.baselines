SELECT
	"rc".RDB$CONSTRAINT_NAME,
	"rc".RDB$RELATION_NAME,
	"rcUq".RDB$RELATION_NAME,
	"thisSeg".RDB$FIELD_NAME,
	"otherSeg".RDB$FIELD_NAME,
	"thisSeg".RDB$FIELD_POSITION
FROM
	RDB$RELATION_CONSTRAINTS "rc"
		INNER JOIN RDB$REF_CONSTRAINTS "refc" ON "rc".RDB$CONSTRAINT_NAME = "refc".RDB$CONSTRAINT_NAME
		INNER JOIN RDB$RELATION_CONSTRAINTS "rcUq" ON "refc".RDB$CONST_NAME_UQ = "rcUq".RDB$CONSTRAINT_NAME
		INNER JOIN RDB$INDEX_SEGMENTS "thisSeg" ON "rc".RDB$INDEX_NAME = "thisSeg".RDB$INDEX_NAME
		INNER JOIN RDB$INDEX_SEGMENTS "otherSeg" ON ("rcUq".RDB$INDEX_NAME = "otherSeg".RDB$INDEX_NAME OR "rcUq".RDB$INDEX_NAME IS NULL AND "otherSeg".RDB$INDEX_NAME IS NULL) AND ("thisSeg".RDB$FIELD_POSITION = "otherSeg".RDB$FIELD_POSITION OR "thisSeg".RDB$FIELD_POSITION IS NULL AND "otherSeg".RDB$FIELD_POSITION IS NULL)
WHERE
	"rc".RDB$CONSTRAINT_TYPE = 'FOREIGN KEY'

