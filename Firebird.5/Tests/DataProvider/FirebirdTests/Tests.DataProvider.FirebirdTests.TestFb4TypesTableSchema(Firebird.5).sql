SELECT
	"r".RDB$RELATION_NAME,
	"r".RDB$RELATION_TYPE,
	"r".RDB$OWNER_NAME,
	"r".RDB$DESCRIPTION
FROM
	RDB$RELATIONS "r"
WHERE
	"r".RDB$SYSTEM_FLAG IS NULL OR CAST("r".RDB$SYSTEM_FLAG AS Int) = 0

